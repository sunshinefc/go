package main

import "fmt"

func main()  {
	var i int32
	var j float32
	var b bool
	var d complex64
	var s string

	fmt.Print("int32 默认值为：")
	fmt.Print(i)
	fmt.Print("\n")

	fmt.Print("float32 默认值为：")
	fmt.Print(j)
	fmt.Print("\n")

	fmt.Print("bool 默认值为：")
	fmt.Print(b)
	fmt.Print("\n")

	fmt.Print("complex64 默认值为：")
	fmt.Print(d)
	fmt.Print("\n")

	fmt.Print("字符串类型 默认值为：")
	fmt.Print(s)
	fmt.Print("\n")


}
